    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)tblVentas.getModel();
        int a=tblVentas.getSelectedRow();
        if(a<0){
            JOptionPane.showMessageDialog(null,"Debes seleccionar una fila de la tabla");
        }else{
            int confirm=JOptionPane.showConfirmDialog(null,"Confirma si desea eliminar registro ");
            if(JOptionPane.OK_OPTION==confirm){
                model.removeRow(a);
                JOptionPane.showMessageDialog(null,"El registro ha sido eliminado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

